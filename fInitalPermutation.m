function [ vValueLeft32, vValueRight32 ] = fInitalPermutation( vBlock64 )
%FEINGANGSPERMUTATION 
    %vBlock64 - 1 64-bit block
    %define initial permutation
    vIP64 = [   58	50	42	34	26	18	10	2 ...
                60	52	44	36	28	20	12	4 ...
                62	54	46	38	30	22	14	6 ...
                64	56	48	40	32	24	16	8 ...
                57	49	41	33	25	17	9	1 ...
                59	51	43	35	27	19	11	3 ...
                61	53	45	37	29	21	13	5 ...
                63	55	47	39	31	23	15	7];
    %shift key by permuation vector (index: destination, value: source)
	for i=1:64
        vBlockPermuted64(i) = vBlock64(vIP64(i));
    end
    %split permuted block in halves
    vValueLeft32 = vBlockPermuted64(1:32);
    vValueRight32= vBlockPermuted64(33:64);
end

